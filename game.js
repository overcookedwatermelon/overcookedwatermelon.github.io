// Initialize game state
let currentIndex = 0;

// Function to display the current story text, image, and counter
function displayStory() {
  const storyText = document.getElementById('story-text');
  const characterImage = document.getElementById('character');
  const counter = document.querySelector('.counter');
  
  storyText.textContent = story[currentIndex].text;
  characterImage.src = story[currentIndex].image;
  counter.textContent = `${currentIndex + 1} out of ${story.length}`;
}

// Function to advance the story forward
function nextStory() {
  currentIndex++;
  if (currentIndex >= story.length) {
    currentIndex = story.length - 1; // Prevent moving beyond the last line
  }
  displayStory();
}

// Function to go back in the story
function previousStory() {
  currentIndex--;
  if (currentIndex < 0) {
    currentIndex = 0; // Prevent moving beyond the first line
  }
  displayStory();
}

// Event listener for advancing the story on mouse left click or tap on mobile
document.addEventListener('click', function(event) {
  if (event.clientX > window.innerWidth / 2) {
    nextStory();
  } else {
    previousStory();
  }
});

// Event listener for advancing the story on arrow key presses
document.addEventListener('keydown', function(event) {
  if (event.key === 'ArrowRight' || event.key === 'ArrowDown') {
    nextStory();
  } else if (event.key === 'ArrowLeft' || event.key === 'ArrowUp') {
    previousStory();
  }
});

// Initial story display
displayStory();